import type { Element, VisualRef } from '../core/types';

/** 技能定义 */
export interface SkillDef {
  id: string;
  name: string;
  element?: Element;       // 无属性 = 纯物理
  type: 'physical' | 'magic' | 'heal' | 'buff';
  targetType: 'enemy' | 'ally' | 'self' | 'aoe_enemy' | 'aoe_ally';
  range: number;           // 像素
  aoeRadius?: number;      // AOE 半径
  mpCost: number;
  cooldown: number;        // 毫秒
  power: number;           // 倍率（× 攻击力）
  learnLevel: number;      // 解锁等级
  effect: VisualRef;       // 技能特效占位
}

/** 视觉占位快捷构造 */
const vis = (
  shape: 'circle' | 'rect' | 'triangle' | 'diamond',
  color: number,
  size: number,
  label?: string,
): VisualRef => ({ kind: 'placeholder', shape, color, size, label });

